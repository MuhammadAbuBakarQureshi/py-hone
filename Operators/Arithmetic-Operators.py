# Addition operator

print(5 + 3) # 8

# Subtraction operator

print(10 - 5) # 5

# Multiplication operator

print(5 * 4) # 20

# Division operator

print(5 / 2) # 2.5

# If we use "//" then numbers after decimal will not be printed
# Floor Division

print(5 // 2) # 2

# Modulo operator
# It gives the remainder

print(5 % 2) # 1


# Power operator

print( 5 ** 2) # 25